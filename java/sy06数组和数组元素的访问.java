package 后端;

public class sy06数组和数组元素的访问 {
    public static void main(String[] args) {
        // 数据的动态初始化
        int[] arr = new int[3];
        // 输出数据名
        System.out.println(arr);
        // 输出数据中的元素
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
    }
}
